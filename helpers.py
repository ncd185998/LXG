from cs50 import SQL
from flask import redirect, render_template, request, session
from functools import wraps

db = SQL("sqlite:///project.db")

def apology(message, code=400):

    user_id = session["user_id"]
    manager_name = db.execute("SELECT manager FROM managers WHERE id = ?", user_id)
    manager_name_real = manager_name[0]["manager"]

    """Render message as an apology to user."""
    def escape(s):
        """
        Escape special characters.

        https://github.com/jacebrowning/memegen#special-characters
        """
        for old, new in [("-", "--"), (" ", "-"), ("_", "__"), ("?", "~q"),
                         ("%", "~p"), ("#", "~h"), ("/", "~s"), ("\"", "''")]:
            s = s.replace(old, new)
        return s
    return render_template("apology.html", top=code, bottom=escape(message), manager_name_real=manager_name_real), code

